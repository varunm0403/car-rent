import mongoose, { Schema } from 'mongoose';
import { ILocation } from '../utils/interfaces/locationInterface';

const locationSchema = new Schema<ILocation>({
  id: {
    type: String,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
    trim: true,
  },
  address: {
    type: String,
    required: true,
    trim: true,
  },
  imageUrl: {
    type: String,
    required: true,
    trim: true,
  },
  lat: {
    type: Number,
    required: true,
  },
  lng: {
    type: Number,
    required: true,
  }
});

export default mongoose.model<ILocation>('Location', locationSchema);